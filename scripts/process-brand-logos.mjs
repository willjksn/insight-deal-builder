/**
 * Removes near-white backgrounds and writes brand assets to public/brand/.
 * Run: node scripts/process-brand-logos.mjs
 */
import fs from "node:fs";
import path from "node:path";
import sharp from "sharp";

const ROOT = path.resolve(import.meta.dirname, "..");
const ASSETS = path.join(
  process.env.USERPROFILE || "",
  ".cursor",
  "projects",
  "c-Projects-Insight-Deal-Builder",
  "assets"
);

const SOURCES = {
  logo: "c__Users_Owner_AppData_Roaming_Cursor_User_workspaceStorage_bb86eecb944408b92c9e5c238f2e0e6f_images_Untitled_design__24_-aed89a19-069b-4003-b039-1c70b2a80541.png",
  icon: "c__Users_Owner_AppData_Roaming_Cursor_User_workspaceStorage_bb86eecb944408b92c9e5c238f2e0e6f_images_Untitled_design__25_-a62a53c4-8ece-40af-87db-0f589090d75a.png",
};

const OUT_DIR = path.join(ROOT, "public", "brand");

function knockOutWhite(data, channels, threshold = 248) {
  for (let i = 0; i < data.length; i += channels) {
    const r = data[i];
    const g = data[i + 1];
    const b = data[i + 2];
    const min = Math.min(r, g, b);
    const max = Math.max(r, g, b);
    const spread = max - min;
    if (min >= threshold && spread <= 12) {
      data[i + 3] = 0;
    } else if (min >= threshold - 14 && spread <= 20) {
      data[i + 3] = Math.min(data[i + 3], Math.round(((threshold - min) / 14) * 255));
    }
  }
}

async function processToPng(inputPath, outputPath, resize) {
  let pipeline = sharp(inputPath).ensureAlpha();
  if (resize) {
    pipeline = pipeline.resize(resize.width, resize.height, {
      fit: "contain",
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    });
  }

  const { data, info } = await pipeline.raw().toBuffer({ resolveWithObject: true });
  knockOutWhite(data, info.channels);

  await sharp(data, {
    raw: { width: info.width, height: info.height, channels: info.channels },
  })
    .png({ compressionLevel: 9, adaptiveFiltering: true })
    .trim({ threshold: 1 })
    .toFile(outputPath);
}

async function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.mkdirSync(path.join(ROOT, "src", "lib", "brand"), { recursive: true });

  const logoOut = path.join(OUT_DIR, "shootspine-logo.png");
  const iconOut = path.join(OUT_DIR, "shootspine-icon.png");
  const icon32Out = path.join(OUT_DIR, "shootspine-icon-32.png");
  const icon180Out = path.join(OUT_DIR, "shootspine-icon-180.png");
  const icon192Out = path.join(OUT_DIR, "shootspine-icon-192.png");
  const icon512Out = path.join(OUT_DIR, "shootspine-icon-512.png");
  const appDir = path.join(ROOT, "src", "app");

  const logoSrc = path.join(ASSETS, SOURCES.logo);
  const iconSrc = path.join(ASSETS, SOURCES.icon);
  const iconFallback = fs.existsSync(iconOut) ? iconOut : null;

  if (fs.existsSync(logoSrc)) {
    await processToPng(logoSrc, logoOut);
  } else if (!fs.existsSync(logoOut)) {
    throw new Error(`Logo source not found and no existing ${logoOut}`);
  }

  const iconInput =
    fs.existsSync(iconSrc) ? iconSrc : iconFallback ?? (() => {
      throw new Error(`Icon source not found under ${ASSETS} and no existing ${iconOut}`);
    })();

  if (fs.existsSync(iconSrc)) {
    await processToPng(iconSrc, iconOut);
  }
  await processToPng(iconInput, icon32Out, { width: 32, height: 32 });
  await processToPng(iconInput, icon180Out, { width: 180, height: 180 });
  await processToPng(iconInput, icon192Out, { width: 192, height: 192 });
  await processToPng(iconInput, icon512Out, { width: 512, height: 512 });

  fs.mkdirSync(appDir, { recursive: true });
  fs.copyFileSync(icon32Out, path.join(appDir, "icon.png"));
  fs.copyFileSync(icon180Out, path.join(appDir, "apple-icon.png"));
  fs.copyFileSync(icon32Out, path.join(ROOT, "public", "favicon.png"));

  const logoMeta = await sharp(logoOut).metadata();
  const logoBuffer = fs.readFileSync(logoOut);
  const logoBase64 = logoBuffer.toString("base64");
  const aspectRatio =
    logoMeta.width && logoMeta.height ? logoMeta.width / logoMeta.height : 4.5;

  const generated = `/** Generated by scripts/process-brand-logos.mjs — do not edit manually */\n\nexport const BRAND_LOGO_PDF_BASE64 = "data:image/png;base64,${logoBase64}";\n\nexport const BRAND_LOGO_ASPECT_RATIO = ${aspectRatio};\n`;

  fs.writeFileSync(path.join(ROOT, "src", "lib", "brand", "logoAssets.generated.ts"), generated);

  console.log(
    "Wrote brand assets to public/brand/, src/app/icon.png, apple-icon.png, public/favicon.png"
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
